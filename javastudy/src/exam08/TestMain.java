package exam08;

interface Flyer{
	public abstract void fly();
}

class Bird implements Flyer{

	@Override
	public void fly() {
		System.out.println("bird fly");
	}
	
}

public interface TestMain {
	public static void main(String[] args) {

		//1. 이름 없는 클래스 사용한 경우
		Flyer f = new Bird(); //다형성
		f.fly();
		
		//2. 익명 클래스 사용한 경우
		/*
		 *  인터페이스명 변수명 = new  인터페이스명(){
		 *  	추상메서드 재정의
		 *  };
		 */
		Flyer f2 = new Flyer() {

			@Override
			public void fly() {
				System.out.println("익명 fly");
			}
			
		};
		f2.fly();
		
	}
	
}
